declare(strict_types=1);

namespace BlackCat\Database\Actions;

use BlackCat\Database\Runtime;
use BlackCat\Database\Support\OperationResult;

/**
 * @api
 *
 * Contract for actions executed against the database runtime.
 * Implementations SHOULD be idempotent and side-effect free where it makes sense
 * (e.g., reads, validations). For mutating operations, implementations SHOULD
 * ensure deduplication/idempotency at the domain level (e.g., via unique keys,
 * idempotency keys, or conditional upserts).
 *
 * Static analysis:
 * - The input is documented as a generic associative array to allow richer shapes
 *   per implementation while preserving a stable callable signature.
 *
 * @template-contravariant TInput of array<string,mixed>
 */
interface ActionInterface
{
    /**
     * Execute the action.
     *
     * Implementations MAY throw domain-specific exceptions; callers should convert
     * them to {@see OperationResult} failures where appropriate.
     *
     * @param Runtime               $rt    Runtime context (connections, tx, config).
     * @param TInput                $input Validated input payload for the action.
     * @return OperationResult             Structured outcome (success/data/error).
     *
     * @throws \Throwable Implementations may throw; callers SHOULD handle and map to OperationResult.
     */
    public function __invoke(Runtime $rt, #[\SensitiveParameter] array $input): OperationResult;
}
