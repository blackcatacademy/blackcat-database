declare(strict_types=1);

namespace BlackCat\Database\Events;

/**
 * @api
 *
 * Minimal, **no-throw** dispatcher contract for CRUD domain events.
 *
 * ## Semantics
 * - Implementations **MUST** catch all listener exceptions (`\Throwable`) and
 *   **MUST NOT** propagate them to the caller. Failures should be logged/reported
 *   to an observability channel (Sentry, OpenTelemetry, etc.).
 * - Listener ordering is implementation-specific; if ordering is not guaranteed,
 *   it should be documented.
 *
 * ## Static analysis
 * - API intentionally stays minimal; validation/serialization happens in `CrudEvent`.
 *
 * @template-covariant TEvent of CrudEvent
 */
interface CrudEventDispatcher
{
    /**
     * Best-effort dispatch — **must never throw**.
     *
     * Implementations should:
     *  - invoke listeners safely and in isolation (try/catch per listener),
     *  - shield callers from side effects of failures,
     *  - (recommended) add diagnostics/telemetry.
     *
     * @param TEvent $event
     * @throws void
     */
    public function dispatch(#[\SensitiveParameter] CrudEvent $event): void;
}
