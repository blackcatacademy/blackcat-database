declare(strict_types=1);

namespace BlackCat\Database\Exceptions;

abstract class DbRuntimeException extends \RuntimeException
{
    /** Optional context for logs/telemetry. */
    protected array $meta = [];
    /** Informational dialect name (e.g., 'postgres'|'mysql'). */
    protected ?string $dialect = null;

    public function __construct(
        string $message = "",
        array $meta = [],
        ?string $dialect = null,
        ?\Throwable $prev = null,
        int $code = 0
    ) {
        parent::__construct($message, $code, $prev);
        $this->meta = $meta;
        $this->dialect = $dialect;
    }

    public function meta(): array { return $this->meta; }
    public function dialect(): ?string { return $this->dialect; }
}
